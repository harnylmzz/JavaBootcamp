package intro;

public class whileDongusu {

	public static void main(String[] args) {

		// While

		int i = 1;

		while (i < 10) {
			System.out.println(i);
			i++;
		}
		System.out.println("While döngüsü bitti.");

		// Do while
		
		int j=1;
		
		do {
			System.out.println(j);
			j+=2;
			
		}while (j < 10); 
			
		System.out.println("Do-While döngüsü bitti.");


	}

}
